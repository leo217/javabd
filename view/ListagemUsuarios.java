    public void readJTable(){
         DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
         modelo.setNumRows(0);
         UsuarioControle usuControle = new UsuarioControle();
         
         for(UsuarioBean u: usuControle.listar()){
             modelo.addRow(new Object[]{
                 u.getId(),
                 u.getLogin(),
                 u.getStatus(),
                 u.getTipo()
             });
         }
    }

